#!/usr/bin/env python3
"""
Module to perform addition of two floats.
"""


def add(a: float, b: float) -> float:
    """
    Function to add two floats.
    Args:
        a (float): The first number.
        b (float): The second number.
    Returns:
        float: The sum of the two numbers.
    """
    return a + b


if __name__ == "__main__":
    add(1.11, 2.22)
